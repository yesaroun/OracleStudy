     , CASE WHEN ROUND(AVG(BASICPAY + SUDANG)) > 2000000 THEN 'A등급'
            WHEN ROUND(AVG(BASICPAY + SUDANG)) BETWEEN 1500000 AND 2000000 THEN 'B등급'
